_SORTED_ALIASES = sorted(PLACE_ALIASES.keys(), key=len, reverse=True)


def lookup_place(text: str) -> str | None:
    """精确匹配别名（整句等于别名）。"""
    key = text.strip()
    if not key:
        return None
    return PLACE_ALIASES.get(key)


def find_place_in_text(text: str) -> tuple[str, str] | None:
    """在句子中查找最长匹配的别名，返回 (alias, canonical)。"""
    stripped = text.strip()
    if not stripped:
        return None
    exact = PLACE_ALIASES.get(stripped)
    if exact:
        return stripped, exact
    for alias in _SORTED_ALIASES:
        if alias in stripped:
            return alias, PLACE_ALIASES[alias]
    return None


def resolve_place_destination(text: str) -> str | None:
    """解析用户输入中的景点/区域目的地。"""
    if not text.strip():
        return None
    found = find_place_in_text(text)
    if found:
        return found[1]
    # 「去洱海」「到鼓浪屿」
    match = re.search(
        r"(?:去|到|想去|要去|玩)([\u4e00-\u9fff]{2,10})(?:玩|看看|旅|游)?",
        text.strip(),
    )
    if match:
        fragment = match.group(1)
        return PLACE_ALIASES.get(fragment) or lookup_place(fragment)
    return None
